# LIST(Keep the order, allow duplicates), (ordered), (mutable), allows changes
X = ["A", "B", "C", "A"]
print(X)

# TUPLE(Keep the order, allow duplicates), (ordered), (immutable), keep data safe
Y = ("A", "B", "C", "A")
print(Y)

# SET(Do not keep the order, do not allow duplicates), (unordered), (mutable), cant update specific items
Z = {"A", "B", "C", "A"}
print(Z)
